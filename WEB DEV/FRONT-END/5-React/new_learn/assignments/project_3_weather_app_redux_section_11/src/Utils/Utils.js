export async function getUserCurrentLocation() {
	try {
		const position = await new Promise((resolve, reject) => {
			navigator.geolocation.getCurrentPosition(resolve, reject);
		});

		const latitude = position.coords.latitude;
		const longitude = position.coords.longitude;
		return { latitude, longitude };
	} catch (error) {
		console.error("Error getting location:", error.message);
		return {};
	}
}
export async function getWeatherAPI(lat, long) {
	const response = await axios.get(`https://api.openweathermap.org/data/2.5/weather?lat=${lat}&lon=${long}&appid=${apiKey}`);

	return response.data;
}
export function formatDate(date) {
	const months = ["January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"];

	const day = date.getDate();
	const month = months[date.getMonth()];
	const year = date.getFullYear();

	// Add ordinal suffix to the day
	const daySuffix = (day) => {
		if (day > 3 && day < 21) return "th"; // 11th, 12th, 13th
		switch (day % 10) {
			case 1:
				return "st";
			case 2:
				return "nd";
			case 3:
				return "rd";
			default:
				return "th";
		}
	};

	return `${month} ${day}${daySuffix(day)} ${year}`;
}

